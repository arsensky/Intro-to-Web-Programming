# 1. Printing by using 'for' and 'while'
print("For loop:")
for i in range(1, 11):
    print(i)

print("While loop:")
i = 1
while i <= 10:
    print(i)
    i += 1

# 2. Iterating the string
string = "Python"
print(f"\nIterating the string: {string}")
for char in string:
    print(char)

# 3. Printing even numbers
number = 2
print("\nEven numbers:")
while number <= 10:
    print(number)
    number += 2